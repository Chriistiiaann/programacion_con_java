/**
 * Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás utilizando
 * un bucle while.
 * 
 * @author Christian Rodríguez Lara
 */
public class T05Ejercicio05 {
  public static void main(String[] args) {
    int i = 340;
    while (i >= 180) {
      i -= 20;
      System.out.println(i);
    }
  }
}
