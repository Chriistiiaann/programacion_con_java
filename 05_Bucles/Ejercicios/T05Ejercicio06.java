/**
 * Muestra los números del 320 al 160, contando de 20 en 20 utilizando un bucle
 * do-while.
 * 
 * 
 * @author Christian Rodríguez Lara
 */
public class T05Ejercicio06 {
  public static void main(String[] args) {
    int i = 340;
    do {
      i -= 20;
      System.out.println(i);
    } while (i >=180);
  }
}
